function xdot = Gravity( ~ , x )

Re  = 6378.137;
J2  = 1.08262668e-3;
mu  = 398600.4418;

r   = norm(x(1:3));

% xdot = [x(4:6)
%         -mu/r^3*x(1:3)];

xdot    = [ x(4);
            x(5);
            x(6);
            -mu*x(1)/r^3*(1+1.5*J2*(Re/r)^2*(1-5*(x(3)/r)^2));
            -mu*x(2)/r^3*(1+1.5*J2*(Re/r)^2*(1-5*(x(3)/r)^2));
            -mu*x(3)/r^3*(1+1.5*J2*(Re/r)^2*(3-5*(x(3)/r)^2))];
end

